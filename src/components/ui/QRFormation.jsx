// Helper to get active module coordinate indices
export function getQRModules(size = 320) {
  const modules = [];
  const rows = QR_MATRIX.length;
  const cols = QR_MATRIX[0].length;
  const moduleSize = size / cols;
  
  // Calculate relative top-left offset to center the QR code
  const offsetX = -size / 2;
  const offsetY = -size / 2;

  for (let r = 0; r < rows; r++) {
    for (let c = 0; c < cols; c++) {
      if (QR_MATRIX[r][c] === 1) {
        modules.push({
          x: offsetX + (c * moduleSize) + (moduleSize / 2),
          y: offsetY + (r * moduleSize) + (moduleSize / 2),
          col: c,
          row: r,
          size: moduleSize
        });
      }
    }
  }

  return modules;
}
